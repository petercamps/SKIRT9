    /** 4-point Lagrange interpolation. */
    inline double lagrange4(const double x[4], const double y[4], double xval)
    {
        double yval = 0.;
        for (int i = 0; i < 4; ++i)
        {
            double l = 1.;
            for (int j = 0; j < 4; ++j)
            {
                if (i != j) l *= (xval - x[j]) / (x[i] - x[j]);
            }
            yval += y[i] * l;
        }
        return yval;
    }

